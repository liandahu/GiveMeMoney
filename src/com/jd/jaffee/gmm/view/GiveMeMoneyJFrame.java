    private void initAddLunchPanel(){
        String[] restaurantNames = RestaurantUtil.getRestaurantNames();
        if(restaurantNames != null && restaurantNames.length > 0){
            restaurantJComboBox.setModel(new DefaultComboBoxModel(restaurantNames));
        }
        this.lunchDateJTextField.setText(DateUtil.getCurrentShortDateString());
        this.lunchWeekJTextField.setText(DateUtil.getDayOfWeek());
        this.lunchUserJPanel.removeAll();
        List<User> users = userService.queryAllUser();
        if(users != null && users.size() > 0){
            for(User user : users){
                JCheckBox box = new JCheckBox();
                box.setText(user.getUserName());
                box.setActionCommand(user.getId() + "");
                box.setVisible(true);
                this.lunchUserJPanel.add(box);
            }
        }
    }

