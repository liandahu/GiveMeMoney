    private void addLunchInfoJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addLunchInfoJButtonActionPerformed
        // TODO add your handling code here:
        int restaurantId = this.restaurantJComboBox.getSelectedIndex();
        if(restaurantId <= 0){
            JOptionPane.showMessageDialog(null, "请选择饭馆");
            this.restaurantJComboBox.requestFocus();
            return;
        }
        String restaurantName = this.restaurantJComboBox.getSelectedItem().toString();
        String lunchDateStr = this.lunchDateJTextField.getText().trim();
        Date lunchDate = null;
        if(StringUtil.isBlank(lunchDateStr)){
            JOptionPane.showMessageDialog(null, "请输入吃饭日期");
            this.lunchDateJTextField.requestFocus();
            return;
        }else{
            try {
                lunchDate = new SimpleDateFormat("yyyy/MM/dd").parse(lunchDateStr);
            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(null, "吃饭日期格式错误，应该是：yyyy/MM/dd");
                this.lunchDateJTextField.requestFocus();
                return;
            }
        }
        String week = this.lunchWeekJTextField.getText().trim();
        if(StringUtil.isBlank(week)){
            JOptionPane.showMessageDialog(null, "请输入吃饭星期");
            this.lunchWeekJTextField.requestFocus();
            return;
        }
        String allMoney = this.allMoneyJTextField.getText().trim();
        if(StringUtil.isBlank(allMoney)){
            JOptionPane.showMessageDialog(null, "请输入饭钱总金额");
            this.allMoneyJTextField.requestFocus();
            return;
        }else{
            try{
                Double.parseDouble(allMoney);
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null, "饭钱总金额是数字");
                this.allMoneyJTextField.setText("");
                this.allMoneyJTextField.requestFocus();
                return;
            }
        }
        String averageMoney = this.averageMoneyJTextField.getText().trim();
        if(!StringUtil.isBlank(averageMoney)){
            try{
                Double.parseDouble(averageMoney);
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null, "人均饭钱是数字");
                this.averageMoneyJTextField.setText("");
                this.averageMoneyJTextField.requestFocus();
                return;
            }
        }
        String userNames = "";
        List<User> users = new ArrayList<User>();
        Component[] components = this.lunchUserJPanel.getComponents();
        if(components != null && components.length > 0){
            for(Component component : components){
                if(component instanceof JCheckBox){
                    JCheckBox box = (JCheckBox) component;
                    if(box.isSelected()){
                        int userId = Integer.parseInt(box.getActionCommand());
                        String userName = box.getText().trim();
                        userNames = userNames + "_" + userName;
                        User user = new User();
                        user.setId(userId);
                        user.setUserName(userName);
                        users.add(user);
                    }
                }
            }
        }
        if(users.isEmpty()){
            JOptionPane.showMessageDialog(null, "请选择吃饭的人");
            return;
        }

        if(StringUtil.isBlank(averageMoney)){
            averageMoney = Double.parseDouble(allMoney)/(users.size()+1) + "";
        }

        LunchInfo info = new LunchInfo();
        info.setAllMoney(Double.parseDouble(allMoney));
        info.setAverageMoney(Double.parseDouble(averageMoney));
        info.setLunchDate(lunchDate);
        info.setRestaurantId(restaurantId);
        info.setRestaurantName(restaurantName);
        info.setUserNames(userNames);
        info.setWeek(week);
        info.setYn(1);

        try{
            boolean result = this.lunchInfoService.addLunchInfo(info);
            if(result){
                result = this.billInfoService.addBillInfos(info, users);
                if(result){
                    JOptionPane.showMessageDialog(null, "添加成功");
                    todayBillInfoJCheckBox.setSelected(true);
                    this.billInfoJMenuItemActionPerformed(evt);
                    return;
                }
            }
        }catch(Exception e){
            e.printStackTrace();
        }
        JOptionPane.showMessageDialog(null, "添加失败");
    }//GEN-LAST:event_addLunchInfoJButtonActionPerformed

