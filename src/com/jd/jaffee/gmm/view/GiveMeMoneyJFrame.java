    private void payBillSubmitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_payBillSubmitJButtonActionPerformed
        // TODO add your handling code here:
        int id = this.payBillId;
        if(id <= 0){
            JOptionPane.showMessageDialog(null, "请选择需要付款的记录！");
            return;
        }
        String money = this.payBillMoneyJTextField.getText().trim();
        try{
            Double.parseDouble(money);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "付款金额是数字！");
            payBillMoneyJTextField.setText("");
            payBillMoneyJTextField.requestFocus();
            return;
        }
        BillInfo info = new BillInfo();
        info.setId(id);
        info.setPayMoney(Double.parseDouble(money));
        boolean result = this.billInfoService.billPay(info);
        if(result){
            JOptionPane.showMessageDialog(null, "付款成功","提示",JOptionPane.INFORMATION_MESSAGE);
            this.payBillJInternalFrame.setVisible(false);
            this.billInfoSubmitJButtonActionPerformed(null);
        }else{
            JOptionPane.showMessageDialog(null, "付款失败","错误",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_payBillSubmitJButtonActionPerformed

    private Map<String, Object> buildCondForBillInfoQuery(){
        Map<String, Object> cond = new HashMap<String, Object>();
        if(todayBillInfoJCheckBox.isSelected()){
            cond.put("today", true);
        }
        String beginDate = billInfoBeginDateJTextField.getText().trim();
        if(!StringUtil.isBlank(beginDate)){
            cond.put("beginDate", beginDate);
        }
        String endDate = billInfoEndDateJTextField.getText().trim();
        if(!StringUtil.isBlank(endDate)){
            cond.put("endDate", endDate);
        }
        if(billInfoWeekJComboBox.getSelectedIndex() > 0){
            cond.put("week", billInfoWeekJComboBox.getSelectedItem().toString());
        }
        if(billInfoPayJComboBox.getSelectedIndex() > 0){
            cond.put("pay", billInfoPayJComboBox.getSelectedIndex()-1);
        }
        String userName = billInfoUserNameJTextField.getText().trim();
        if(!StringUtil.isBlank(userName)){
            cond.put("userName", userName);
        }
        return cond;
    }
    private void initBillInfoJInternalFrame(){
        billInfoBeginDateJTextField.setText("");
        billInfoWeekJComboBox.setSelectedIndex(0);
        billInfoPayJComboBox.setSelectedIndex(0);
        billInfoUserNameJTextField.setText("");
        String[] colunmName = new String[]{"编号","姓名","日期/星期","饭馆","总数","人均","是否付款","付款金额","付款时间"};
        Object[][] dataVector = new Object[1][9];
        List<BillInfo> infos = null;
        DefaultTableModel tableModel = new GmmTableModel();
        try{
            Map<String, Object> cond = this.buildCondForBillInfoQuery();
            infos = this.billInfoService.queryBillInfoByCond(cond);
        }catch(Exception e){
            e.printStackTrace();
        }
        if(infos != null && infos.size() > 0){
            dataVector = new Object[infos.size()][9];
            DateFormat df = new SimpleDateFormat("yyyy/MM/dd");
            DateFormat dfPayTime = new SimpleDateFormat("yyyy/MM/dd hh:MM:ss");
            BillInfo info = null;
            for(int index = 0; index < infos.size(); index++){
                info = infos.get(index);
                dataVector[index][0] = info.getId();
                dataVector[index][1] = info.getUser().getUserName();
                dataVector[index][2] = df.format(info.getLunchInfo().getLunchDate()) + "/" + info.getLunchInfo().getWeek();
                dataVector[index][3] = info.getLunchInfo().getRestaurantName();
                dataVector[index][4] = info.getLunchInfo().getAllMoney();
                dataVector[index][5] = info.getLunchInfo().getAverageMoney();
                dataVector[index][6] = info.getPay() == 0 ? "<html><font color=red>否</font></html>" : "<html><font color=blue>是</font></html>";
                dataVector[index][7] = info.getPayMoney() <= 0 ? "" : info.getPayMoney();
                dataVector[index][8] = info.getPayTime() == null ? "" : dfPayTime.format(info.getPayTime());
            }
        }
        tableModel.setDataVector(dataVector, colunmName);
        billInfoJTable.setModel(tableModel);
        adjustBillInfoJTableSize(billInfoJTable);
        this.billInfoJInternalFrame.setVisible(true);
    }

