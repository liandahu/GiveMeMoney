    private void queryLunchSubmitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_queryLunchSubmitJButtonActionPerformed
        // TODO add your handling code here:
        Map<String, Object> cond = new HashMap<String, Object>();
        int restaurantId = queryLunchResJComboBox.getSelectedIndex();
        if(restaurantId > 0){
            cond.put("restaurantId", restaurantId);
        }
        String beginDate = queryLunchBtJTextField.getText().trim();
        if(!StringUtil.isBlank(beginDate)){
            cond.put("beginDate", beginDate);
        }
        String endDate = queryLunchEtJTextField.getText().trim();
        if(!StringUtil.isBlank(endDate)){
            cond.put("endDate", endDate);
        }
        String week = queryLunchInfoWeekJComboBox.getSelectedItem().toString();
        if(!StringUtil.isBlank(week)){
            cond.put("week", week);
        }
        List<LunchInfo> infos = this.lunchInfoService.queryLunchInfoByCond(cond);
        lunchInfoJTable.removeAll();
        String[] colunmName = new String[]{"编号","日期","星期","饭馆","总数","人均","名单"};
        Object[][] dataVector = new Object[1][7];
        DefaultTableModel tableModel = new GmmTableModel();
        if(infos != null && infos.size() > 0){
            dataVector = new Object[infos.size()][7];
            DateFormat df = new SimpleDateFormat("yyyy/MM/dd");
            for(int index = 0; index < infos.size(); index++){
                LunchInfo info = infos.get(index);
                dataVector[index][0] = info.getId();
                dataVector[index][1] = df.format(info.getLunchDate());
                dataVector[index][2] = info.getWeek();
                dataVector[index][3] = info.getRestaurantName();
                dataVector[index][4] = info.getAllMoney();
                dataVector[index][5] = info.getAverageMoney();
                dataVector[index][6] = info.getUserNames();
            }
        }
        tableModel.setDataVector(dataVector, colunmName);
        lunchInfoJTable.setModel(tableModel);
        adjustLunchInfoTableColumnSize(lunchInfoJTable);
        lunchInfoJTable.setEnabled(false);
    }//GEN-LAST:event_queryLunchSubmitJButtonActionPerformed

